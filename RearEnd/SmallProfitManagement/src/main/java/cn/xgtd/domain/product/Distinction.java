package cn.xgtd.domain.product;

/**
 * @author Kite
 * @date 2020/6/18
 */
public class Distinction {
    /**商品id**/
    private Integer productId;
    /**尺寸id**/
    private Integer sizeId;
    /**颜色id**/
    private Integer colourId;
    /**套餐id**/
    private Integer comboId;
    /**规格id**/
    private Integer specificationId;
    /**口味id**/
    private Integer tasteId;
    /**种类id**/
    private Integer kindId;
    /**版本id**/
    private Integer versionIfId;

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public Integer getSizeId() {
        return sizeId;
    }

    public void setSizeId(Integer sizeId) {
        this.sizeId = sizeId;
    }

    public Integer getColourId() {
        return colourId;
    }

    public void setColourId(Integer colourId) {
        this.colourId = colourId;
    }

    public Integer getComboId() {
        return comboId;
    }

    public void setComboId(Integer comboId) {
        this.comboId = comboId;
    }

    public Integer getSpecificationId() {
        return specificationId;
    }

    public void setSpecificationId(Integer specificationId) {
        this.specificationId = specificationId;
    }

    public Integer getTasteId() {
        return tasteId;
    }

    public void setTasteId(Integer tasteId) {
        this.tasteId = tasteId;
    }

    public Integer getKindId() {
        return kindId;
    }

    public void setKindId(Integer kindId) {
        this.kindId = kindId;
    }

    public Integer getVersionIfId() {
        return versionIfId;
    }

    public void setVersionIfId(Integer versionIfId) {
        this.versionIfId = versionIfId;
    }
}
